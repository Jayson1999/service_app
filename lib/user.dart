class User {
   String _name;
   String _email;
   String _hp;

   String get hp => _hp;

   set hp(String value) {
     _hp = value;
   }

   String get email => _email;

   set email(String value) {
     _email = value;
   }

   String get name => _name;

   set name(String value) {
     _name = value;
   }


}